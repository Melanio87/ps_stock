    private void Guardar() {
        String sql;
        PreparedStatement pst;
        Coneccion con = new Coneccion();
        try {
            int res=0;
            if (!txCanti.getText().isEmpty()) {
                
                int can = Integer.parseInt(txCanti.getText());
                for(int i=0;i<can;i++){
                    sql = "INSERT INTO mesa(m_descri) values(?)";
                    pst = con.getCon().prepareCall(sql);
                    pst.setString(1, txDescri.getText().trim()+" "+(i+1));
                    res+= pst.executeUpdate();
                }
                
                if(res>0){
                    JOptionPane.showMessageDialog(null, res+" Mesas registradas");
                }
            } 
            con.close();
        } catch (SQLException | HeadlessException e) {
                System.out.println("error "+e.getMessage());
        }
    }

