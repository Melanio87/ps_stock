    public void consultarCliente() {
        String sql;

        sql = "select cli_codigo,cli_nombre,cli_ruc from "
                + "clientes  "
                + "  where cli_codigo =" + txCodcli.getText() + "   order by cli_codigo";

        Coneccion con = new Coneccion();
        ResultSet rs;
        Clientes cli = null;

        try {
            con.consulta(sql);
            rs = con.getRs();
            if (rs.next()) {
                txdescli.setText(rs.getString("cli_nombre"));
                txRUC.setText(rs.getString("cli_ruc"));
            }

            con.close();
        } catch (Exception e) {
            if (con != null) {
                con.close();
            }
        }
    }

    public void consultarMoneda() {
        String sql;
        sql = "select mon_codigo,mon_dolar,mon_pes,mon_real,mon_fecha from moneda";
        Coneccion con = new Coneccion();
        ResultSet rs;
        try {
            con.consulta(sql);
            rs = con.getRs();
            if (rs.next()) {
                dol = rs.getDouble("mon_dolar");
                rea = rs.getDouble("mon_real");
                pes = rs.getDouble("mon_pes");
            }

            con.close();
        } catch (Exception e) {
            if (con != null) {
                con.close();
            }
        }
    }

