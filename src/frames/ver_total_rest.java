    private void GuaVenta() {
        int tipven;
        try {

            ResultSet rs;
            int codvent = 0;

            String Hora = new SimpleDateFormat("HH:mm:ss").format(Calendar.getInstance().getTime());
            Cfecha = Calendar.getInstance();
            fecha = sdf.format(Cfecha.getTime());

            Venta ven = getVenta();
            ven.setVen_cliente(Integer.parseInt(txCodcli.getText()));

            Numero_a_Letra nl = new Numero_a_Letra();
            DecimalFormat format = new DecimalFormat("#.##");
            //System.out.println("Letra " + nl.Convertir(format.format(ven.getVen_total()), true));
            ven.setVen_totlet(nl.Convertir(format.format(ven.getVen_total()), true));

            coneccion = new Coneccion();
            con = coneccion.getCon();
            Statement stmt;

            con.setAutoCommit(false);
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            int numfac = 0;
            String sql;
            if (jRComun.isSelected()) {
                this.setTipfac(1);
                try {
                    if (txEntrega.getText().isEmpty()) {
                        ven.setVen_efectivo(0.0);
                        ven.setVen_vuelto(0.0);
                    } else {
                        ven.setVen_efectivo(df.parse(txEntrega.getText()).doubleValue());
                        ven.setVen_vuelto(df.parse(txVuelto.getText()).doubleValue());
                    }
                } catch (ParseException ex) {
                    Logger.getLogger(ver_total_rest.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                this.setTipfac(2);
                sql = "select (emp_numfac3+1) as numfac,(to_char(emp_numfac1, 'FM999099')||'-'||to_char(emp_numfac2, 'FM999099')||'-'||to_char((emp_numfac3+1), 'FM9990999999')) AS des_numfac from empresa";
                rs = stmt.executeQuery(sql);

                if (rs.next()) {
                    numfac = rs.getInt("numfac");
                    ven.setNunfac(rs.getString("des_numfac"));
                    try {
                        if (txEntrega.getText().isEmpty()) {
                            ven.setVen_efectivo(0.0);
                            ven.setVen_vuelto(0.0);
                        } else {
                            ven.setVen_efectivo(df.parse(txEntrega.getText()).doubleValue());
                            ven.setVen_vuelto(df.parse(txVuelto.getText()).doubleValue());
                        }
                    } catch (ParseException ex) {
                        Logger.getLogger(ver_total_rest.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
                stmt.executeUpdate("update empresa set emp_numfac3=" + numfac);
            }
            
            ven.setVen_estado(true);
            
             setNumven(ven.getVen_codigo());
            
            sql = "Update ventas set ven_fecha = '" + ven.getVen_fecha() + "', "
                    + "ven_hora = '" + ven.getVen_hora() + "', "
                    + "ven_codven = " + ven.getVen_codven() + ", "
                    + "ven_total = " + ven.getVen_total() + ", "
                    + "ven_cliente = " + ven.getVen_cliente() + ", "
                    + "ven_tipo= " + ven.getTipven() + ","
                    + "ven_numfac= '" + ven.getNunfac() + "', "
                    + "ven_efectivo= " + ven.getVen_efectivo() + ","
                    + "ven_vuelto= " + ven.getVen_vuelto() + ", "
                    + "ven_descuento= " + ven.getVen_descuento() + ", "
                    + "ven_totlet= '" + ven.getVen_totlet() + "' ,"
                    + "ven_estado= " + ven.isVen_estado() + " "
                    + "where ven_codigo =" + ven.getVen_codigo();

            stmt.executeUpdate(sql);
            
            stmt.executeUpdate("update mesa set m_estatus= " + 1
                        + "where m_codigo=" + ven.getVen_mesa());

            con.commit();
            con.close();
            setSitu(1);
            this.dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al registrar Ventas " + e.getMessage());
            //System.out.println(e.getMessage());
        }
    }

