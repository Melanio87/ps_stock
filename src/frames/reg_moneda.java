    public void consultarMoneda() {
        String sql;
        sql = "select mon_codigo,mon_dolar,mon_pes,mon_real,mon_fecha from moneda";
        Coneccion con = new Coneccion();
        ResultSet rs;
        try {
            con.consulta(sql);
            rs = con.getRs();
            if (rs.next()) {
                cod = rs.getInt("mon_codigo");
                txDolar.setText(df.format(rs.getDouble("mon_dolar")));
                txReal.setText(df.format(rs.getDouble("mon_real")));
                txPeso.setText(df.format(rs.getDouble("mon_pes")));
            }
            con.close();
        } catch (Exception e) {
            if (con != null) {
                con.close();
            }
        }
    }

    public void insertMoneda() {
        try {
            String sql;
            if (cod == 0) {
                sql = "insert into moneda(mon_dolar,mon_real,mon_pes) values("
                        + df.parse(txDolar.getText()).doubleValue() + ","
                        + df.parse(txReal.getText()).doubleValue() + ","
                        + df.parse(txPeso.getText()).doubleValue() + ")";
            } else {
                sql = "update  moneda set mon_dolar=" + df.parse(txDolar.getText()).doubleValue()
                        + ",mon_real=" + df.parse(txReal.getText()).doubleValue()
                        + ",mon_pes=" + df.parse(txPeso.getText()).doubleValue();
            }

            Coneccion con = new Coneccion();
            try {
                int res = con.update(sql);

                if (res == 1) {
                    JOptionPane.showMessageDialog(null, "Datos Insertados");
                }
                con.close();
            } catch (Exception e) {
                if (con != null) {
                    con.close();
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(reg_moneda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

