    private void txEntregaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txEntregaFocusLost
        try {
            entrega=df.parse(txEntrega.getText()).doubleValue();
        } catch (ParseException ex) {
            Logger.getLogger(ven_cobro.class.getName()).log(Level.SEVERE, null, ex);
        }
       saldo= getTotal() - entrega;
       txSaldo.setText(df.format(saldo));
    }//GEN-LAST:event_txEntregaFocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        deucli d = new deucli();
        d.setDcl_cancuo(Integer.parseInt(txCancuo.getText()));
        d.setDcl_monto(saldo);
        this.setDeu(d);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txCancuoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txCancuoKeyTyped
         char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)
                && (c != '.')) {
            evt.consume();
        }
        if (c == '.' && txCancuo.getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_txCancuoKeyTyped

    private void txEntregaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txEntregaKeyPressed
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            txCancuo.grabFocus();
        }
    }//GEN-LAST:event_txEntregaKeyPressed

    private void txCancuoFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txCancuoFocusGained
        txCancuo.setText("1");
        txCancuo.selectAll();
        txMonCuo.setText(txSaldo.getText());
    }//GEN-LAST:event_txCancuoFocusGained

    private void txEntregaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txEntregaKeyReleased
        try {
        if(!txEntrega.getText().isEmpty()){
             txEntrega.setText(df.format(df.parse(txEntrega.getText())));
        }
        }catch (ParseException ex) {
            Logger.getLogger(ver_total.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txEntregaKeyReleased

