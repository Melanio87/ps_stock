    public void consultarCliente() {
        String sql;

        sql = "select cli_codigo,cli_nombre,cli_ruc from "
                + "clientes  "
                + "  where cli_codigo =" + txCodcli.getText() + "   order by cli_codigo";

        Coneccion con = new Coneccion();
        ResultSet rs;
        Clientes cli = null;

        try {
            con.consulta(sql);
            rs = con.getRs();
            if (rs.next()) {
                txdescli.setText(rs.getString("cli_nombre"));
                txRUC.setText(rs.getString("cli_ruc"));
            }

            con.close();
        } catch (Exception e) {
            if (con != null) {
                con.close();
            }
        }
    }

    public boolean updateVenta() {
        String sql;
        coneccion = new Coneccion();
        con = coneccion.getCon();
        Statement stmt;
        ResultSet rs;
        try {
            con.setAutoCommit(false);
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            sql = "select  ven_numfac from ventas where ven_codigo=" + txNumVent.getText() + " and ven_numfac = 'null' and ven_situ=1";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                sql = "select (emp_numfac3+1) as numfac,(to_char(emp_numfac1, 'FM999099')||'-'||to_char(emp_numfac2, 'FM999099')||'-'||to_char((emp_numfac3+1), 'FM9990999999')) AS des_numfac from empresa";
                rs = stmt.executeQuery(sql);
                int numfac = 0;

                if (rs.next()) {
                    numfac = rs.getInt("numfac");
                }
                stmt.executeUpdate("update empresa set emp_numfac3=" + numfac);

                sql = "update ventas set ven_cliente=" + txCodcli.getText()
                        + ", ven_numfac=" + numfac + " where ven_codigo=" + txNumVent.getText();
                stmt.executeUpdate(sql);
                con.commit();
                con.close();
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Factura legal ya fue impresa");
                con.commit();
                con.close();
                return false;
            }
        } catch (SQLException | HeadlessException e) {
            return false;
        }
    }
    
        public boolean shearchVenta() {
        String sql;
        coneccion = new Coneccion();
        con = coneccion.getCon();
        Statement stmt;
        ResultSet rs;
        try {
            con.setAutoCommit(false);
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            sql = "select  ven_numfac from ventas where ven_codigo=" + txNumVent.getText() + " and ven_numfac = 'null' and ven_situ=1";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                con.commit();
                con.close();
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Factura legal ya fue impresa");
                con.commit();
                con.close();
                return false;
            }
        } catch (SQLException | HeadlessException e) {
            return false;
        }
    }

    private void imprimir(int codven) throws ParseException {
        JasperReport reporte;
        JasperPrint reporte_view;
        coneccion = new Coneccion();
        con = coneccion.getCon();
        try {
            //archi = this.getClass().getResource("/reports/inf_factura.jasper");
            reporte = (JasperReport) JRLoader.loadObject(archi);
            Map<String, Object> param = new HashMap<>();
            param.put("NUMVEN", codven);
            reporte_view = JasperFillManager.fillReport(reporte, param, con);
            //JasperViewer.viewReport(reporte_view, false);
            JasperPrintManager.printReport(reporte_view, false);
            /*JasperPrintManager print1 = new JasperPrintManager();
             print1.printReport(reporte_view,false);*/
            //terminamos la conexion a la base de datos
            con.close();
            //limpiar();
        



} catch (JRException E) {
        } catch (SQLException ex) {
            Logger.getLogger(Ventas.class  

.getName()).log(Level.SEVERE, null, ex);
        }
    }

